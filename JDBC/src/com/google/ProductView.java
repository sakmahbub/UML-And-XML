    private void btnSearchProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchProductActionPerformed
        // TODO add your handling code here:

        try {
            ResultSet rs = UtilsCaAndPro.getProductDataById(Integer.parseInt(txtIdProduct.getText()));
            while (rs.next()) {
                txtIdProduct.setText(String.valueOf(rs.getInt(1)));
                txtNameProduct.setText(rs.getString(2));
                txtQua.setText(String.valueOf(rs.getInt(3)));
                txtUPrice.setText(String.valueOf(rs.getDouble(4)));
                txtToPrice.setText(String.valueOf(rs.getDouble(5)));
                txtDate.setText(String.valueOf(rs.getDate(6)));
                txtCatId.setText(String.valueOf(rs.getInt(7)));

                lblDisplayProduct.setText("Data Show Successfully");

            }

        } catch (Exception e) {
        }

    }//GEN-LAST:event_btnSearchProductActionPerformed

