    public void displayCategoryList() {
        List<Category> categorys = Lists.getCategoryList();

//        Category cat = categorys.get(categorys.size() - 1);
//        txtIdCategory.setText(String.valueOf(cat.getId() + 1));
        DefaultTableModel model = (DefaultTableModel) tblDisplayCategory.getModel();
        model.setRowCount(0);
        Object[] row = new Object[2];
        for (int i = 0; i < categorys.size(); i++) {
            row[0] = categorys.get(i).getId();
            row[1] = categorys.get(i).getName();
            model.addRow(row);

        }

    }

    public void displayProductList() {
        List<Product> products = Lists.getProductList();

//        Product pro = products.get(products.size() - 1);
//        txtIdProduct.setText(String.valueOf(pro.getId() + 1));
        DefaultTableModel model = (DefaultTableModel) tblDisplayProduct.getModel();
        model.setRowCount(0);
        Object[] row = new Object[7];
        for (int i = 0; i < products.size(); i++) {
            row[0] = products.get(i).getId();
            row[1] = products.get(i).getName();
            row[2] = products.get(i).getQuantity();
            row[3] = products.get(i).getUnitPrice();
            row[4] = products.get(i).getTotalPrice();
            row[5] = products.get(i).getParchaseDate();
            row[6] = products.get(i).getCategory().getId();
            model.addRow(row);

        }

    }


